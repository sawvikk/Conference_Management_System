    <h2 class="text-uppercase fw-bold text-center" data-aos="fade-up">
        <span class="secondary_color">About</span> The Event
    </h2>
    <p class="text-muted" style="text-align: justify" data-aos="fade-right">
        After making the dream of Digital Bangladesh a reality, the
        government is currently working on building Smart Bangladesh with
        the slogan “Inclusive Digital Transformation to Build a Developed
        and Prosperous Country by 2041”. To realize this goal, four key
        pillars have been established: Smart Citizens, Smart Government,
        Smart Economy, and Smart Society. All these subjects claim
        thoughtful discussions and dialogues among researchers, scientists,
        academics, and practitioners across the world. In this context,
        Jatiya Kabi Kazi Nazrul Islam University is going to host the 2nd
        international conference on Technology, Business, and Justice,
        ICTBJ-2023, the first of its kind.
    </p>

    <p class="text-muted text-justify" style="text-align: justify" data-aos="fade-right">
        This conference will bring together a wide range of researchers,
        academics, experts, and practitioners from the fields of science,
        business, and legal studies for a comprehensive discussion, debate,
        knowledge-sharing and collaboration on a journey towards Smart
        Bangladesh in a compassionate, thoughtful, and attentive atmosphere.
        Consequently, the maiden ICTBJ-2023 encourages researchers and
        leaders across the globe to submit their research papers that
        investigate the potential of innovation, trade and social justice.
    </p>
